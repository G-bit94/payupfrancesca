                <p>After some brief chat on Reddit and later WhatsApp, Francesca and I agreed upon the scope
                    of
                    work, timeline, and compensation for the different projects I'd be undertaking. I was to
                    dedicate as much time as I could to help her develop several pet projects she'd been working on by
                    taking them from ideas into platforms that could earn her money. Of course, in return,
                    Francesca committed to providing (what I then deemed) fair remuneration for my efforts ($4/hr). With
                    the terms
                    clearly outlined and both parties in agreement,
                    I enthusiastically began working on <strong>Yupvoted</strong>, her first project.</p>
